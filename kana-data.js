// ひらがな→カタカナはユニコード上 +0x60 の関係。じっさいに変換して使う。
function hiraganaToKatakana(text) {
  return text.replace(/[ぁ-ゖ]/g, (ch) =>
    String.fromCharCode(ch.charCodeAt(0) + 0x60)
  );
}

function katakanaToHiragana(text) {
  return text.replace(/[ァ-ヶ]/g, (ch) =>
    String.fromCharCode(ch.charCodeAt(0) - 0x60)
  );
}
