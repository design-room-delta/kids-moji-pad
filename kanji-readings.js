// 辞書のキーを長い順に並べておき、複合語を先に一致させる
// (例:「大好き」を「大」＋「好き」に分割してしまわないようにする)
const KANJI_KEYS_BY_LENGTH = Object.keys(KANJI_READINGS).sort(
  (a, b) => b.length - a.length
);

function convertKnownKanjiToKana(text) {
  let result = "";
  let i = 0;
  outer: while (i < text.length) {
    for (const key of KANJI_KEYS_BY_LENGTH) {
      if (text.startsWith(key, i)) {
        result += KANJI_READINGS[key];
        i += key.length;
        continue outer;
      }
    }
    result += text[i];
    i += 1;
  }
  return result;
}
